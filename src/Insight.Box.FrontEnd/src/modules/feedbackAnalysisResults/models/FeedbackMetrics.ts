/**
 * Represents metrics related to feedback.
 */
export class FeedbackMetrics {
    /**
     * The Net Promoter Score (NPS) metric.
     */
    public nps: number;

    /**
     * The Customer Satisfaction (CSAT) metric.
     */
    public csat: number;

    /**
     * The Customer Effort Score (CES) metric.
     */
    public ces: number;
}